name = "Kyle"

print(len(name)) #Length
print(name.find("y")) #Find character in a string
print(name.capitalize()) #Capitalise first letter of string
print(name.upper()) #all caps
print(name.lower()) #all lower case
print(name.isdigit()) #is number?
print(name.isalpha()) #contains only letters?
print(name.count("e")) #count of specific letter
print(name.replace("e","ie")) #Change letters
print(name*3) #Print multiple times